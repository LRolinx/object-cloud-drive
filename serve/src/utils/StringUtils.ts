export class StringUtils {
  private constructor() {
    //
  }

  /**
   * 验证一个指定的字符串是否为空
   * @param o 需要验证的字符串对象
   * @returns 验证结果
   */
  static hasText(o?: string): boolean {
    if (TypeCheck.getType(o) != '[object String]' || o == void 0) {
      return false;
    }
    return o.trim().length > 0;
  }
}
